    function normalizeUiLang(lang) {
      const s = String(lang || '').trim().toLowerCase();
      if (!s) return 'en';
      if (s === 'ur' || s.startsWith('ur-')) return 'ur';
      return 'en';
    }

    function getStoredUiLang() {
      try {
        const v = localStorage.getItem(UI_LANG_KEY);
        if (!v) return '';
        return normalizeUiLang(v);
      } catch {
        return '';
      }
    }

    function detectUiLang() {
      const stored = getStoredUiLang();
      if (stored) return stored;
      const nav = (navigator.languages && navigator.languages[0]) ? navigator.languages[0] : navigator.language;
      return normalizeUiLang(nav);
    }

    function t(key, vars) {
      const k = String(key || '').trim();
      if (!k) return '';
      const lang = I18N.current || 'en';
      const dict = (I18N.dict && I18N.dict[lang]) ? I18N.dict[lang] : {};
      const en = (I18N.dict && I18N.dict.en) ? I18N.dict.en : {};
      let s = dict[k] ?? en[k] ?? k;
      if (vars && typeof vars === 'object') {
        for (const [name, value] of Object.entries(vars)) {
          s = String(s).replaceAll(`{${name}}`, String(value));
        }
      }
      return String(s);
    }

    function applyI18nToDom() {
      const lang = I18N.current || 'en';
      const dir = (lang === 'ur') ? 'rtl' : 'ltr';
      try {
        document.documentElement.lang = lang;
        document.documentElement.dir = dir;
      } catch {}

      try { document.title = t('doc.title'); } catch {}

      // textContent
      for (const el of document.querySelectorAll('[data-i18n]')) {
        const k = el.getAttribute('data-i18n');
        if (!k) continue;
        el.textContent = t(k);
      }
      // placeholder
      for (const el of document.querySelectorAll('[data-i18n-placeholder]')) {
        const k = el.getAttribute('data-i18n-placeholder');
        if (!k) continue;
        try { el.setAttribute('placeholder', t(k)); } catch {}
      }
      // title
      for (const el of document.querySelectorAll('[data-i18n-title]')) {
        const k = el.getAttribute('data-i18n-title');
        if (!k) continue;
        try { el.setAttribute('title', t(k)); } catch {}
      }

      // Best-effort: update the language selector label/value.
      const sel = document.getElementById('uiLangSelect');
      if (sel) {
        try { sel.value = lang; } catch {}
      }

      try { applySidebarMenuState(); } catch {}
    }

    function setUiLanguage(lang, opts) {
      const o = (opts && typeof opts === 'object') ? opts : {};
      const next = normalizeUiLang(lang);
      I18N.current = next;
      if (o.persist !== false) {
        try { localStorage.setItem(UI_LANG_KEY, next); } catch {}
      }
      applyI18nToDom();
      try {
        if (typeof window.__PERCEPTA_EVENTS_COLUMNS_RERENDER === 'function') {
          window.__PERCEPTA_EVENTS_COLUMNS_RERENDER();
        }
      } catch {}
      try {
        // Repaint dynamic areas that may embed translated strings.
        state.dirty.counters = true;
        state.dirty.agents = true;
        state.dirty.tables = true;
        state.dirty.details = true;
        state.dirty.health = true;
        state.dirty.ws = true;
        state.dirty.escalations = true;
        state.dirty.auth = true;
        scheduleRender();
      } catch {}
    }

    function initI18n() {
      // Keep initial UI consistent across refreshes.
      const initial = detectUiLang();
      setUiLanguage(initial, { persist: false });
    }

